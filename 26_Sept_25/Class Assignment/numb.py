# Arithmetic Expressions using variables

# Assigning values to variables
a = 15
b = 4

print("Values: a =", a, ", b =", b)

# Addition
sum_result = a + b
print("Addition (a + b) =", sum_result)

# Subtraction
sub_result = a - b
print("Subtraction (a - b) =", sub_result)

# Multiplication
mul_result = a * b
print("Multiplication (a * b) =", mul_result)

# Division
div_result = a / b
print("Division (a / b) =", div_result)

# Modulus (remainder)
mod_result = a % b
print("Modulus (a % b) =", mod_result)

# Exponentiation (power)
exp_result = a ** b
print("Exponentiation (a ** b) =", exp_result)

# Floor Division (integer division)
floor_div_result = a // b
print("Floor Division (a // b) =", floor_div_result)

# A combined expression
combined = (a + b) * (a - b) / b
print("Combined Expression ((a + b) * (a - b) / b) =", combined)
